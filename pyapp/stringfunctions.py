# STRING FUNCTIONS

myStr = 'hellothereworld'

# capitalize
print(myStr.capitalize())

# swap case
print(myStr.swapcase())

# get length
print(len(myStr))

# Replace
print(myStr.replace('world', 'Everyone'))

sub = "o"
print(myStr.count(sub))


# Startswith
print(myStr.startswith('hello')) 

# endswith
print(myStr.endswith('world'))

# Split to list
print(myStr.split())

# find
print(myStr.find('x'))

# index 
print(myStr.index('h'))

# Is all alphanumeric
print(myStr.isalnum())

# Is all aphabetic
print(myStr.isalpha())

# Is all numeric
print(myStr.isnumeric())

